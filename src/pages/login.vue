<script>
	import logoImg from "../assets/img/logo.png";
  import axios from 'axios';

	export default {
    name:"Login",
    data(){
      return {
        logo:logoImg,
        loginForm: {
          eno: '',
          password: ''
        },
        rules: {
          eno: [
            { required: true, message: '请输入用户名', trigger: 'blur' },
            { min: 2, max: 8, message: '长度在 2 到 8 个字符', trigger: 'blur' }
              ],
          password: [
            { required: true, message: '请输入密码', trigger: 'blur' }
          ],
        }
      }
    },
    mounted(){
      let _this = this;
      document.onkeydown = function(e) {   //按下回车提交
        let key = window.event.keyCode;
        //事件中keycode=13为回车事件
        if (key == 13) {
          _this.submitForm();
        }
      };
    },
    methods: {
      loginByWechat(){
      },
      showMessage(type,message){
                this.$message({
                    type: type,
                    message: message
                });
            },
      submitForm() {
        debugger
        //判断表单验证是否通过，然后提交数据到后台
        this.$refs["loginForm"].validate((valid) => {
          if (valid) {
            // this.showMessage('success',""+this.loginForm.eno+this.loginForm.password)
            // 提交
            axios.post("/login",this.loginForm).then(res => {
              if (res.data[0] == 0){
                //
                sessionStorage.setItem("eno",res.data[1]["eno"])
                sessionStorage.setItem("ename",res.data[1]["ename"])
                sessionStorage.setItem("is_admin",res.data[1]["isAdmin"])
                sessionStorage.setItem("avatar_url",res.data[1]["avatarUrl"])
                sessionStorage.setItem("rank",res.data[1]["rank"])
                sessionStorage.setItem("title",res.data[1]["title"])
                sessionStorage.setItem("dailyAllowance",res.data[1]["dailyAllowance"])
                this.$router.push({path: "/home/ticketList"})
                this.showMessage("success","登录成功")
                // this.$message({message: "登录成功",type: 'success'})
                // this.$store.dispatch('initLeftMenu'); //设置左边菜单始终为展开状态
              }else{
                this.showMessage("error","用户名或密码错误")
                // this.$message({message: "用户名或密码错误",type: 'error'})
              }
            })
          }else {
            this.showMessage("error","用户名或密码不能为空")
            // this.$message({message: "用户名或密码不能为空",type: 'error'})
          }
        });
      }
    }
	}
</script>

